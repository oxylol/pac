int random(int max,int min) {
	int r = rand() % (max - min + 1);
	r = r + min;
	return r;
}
void points() {
	for (int l = 0; l < 30; l++)
	{
		point[0][l] = 0;
		point[1][l] = 0;
	}
	for (int a = 0; a < maxX; a++) {
		for (int b = 0; b < maxY; b++) {
			if (plan[a][b] == 2) {
			plan[a][b] = 0;
			}
		}

	}

	for (int i = 0; i < 30; i++)
	{
	int rY = random(maxY, 0);
	int rX = random(maxX, 0);

	if (cntrl(rY,rX,0,0,1) == true)
	{
		plan[rX][rY] = 2;
		point[0][i] = rX;
		point[1][i] = rY;
	}
	else
	{
		i--;
	}

	}

}


